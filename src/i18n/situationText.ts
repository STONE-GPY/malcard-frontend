function getLanguage(language: string): SituationLanguage {
  if (language.startsWith('en')) return 'en';
  if (language.startsWith('ru')) return 'ru';
  return 'ko';
}

function getSituationText(
  kind: SituationTextKind,
  situation: Situation,
  language: string,
  fallback: string,
): string {
  const lang = getLanguage(language);
  if (lang === 'ko') return fallback;

  if (kind === 'title') {
    return titleText[lang][situation.id] ?? fallback;
  }
  if (kind === 'unit') {
    return unitText[lang][situation.unit_title ?? ''] ?? fallback;
  }
  return locationText[lang][situation.location] ?? fallback;
}

export function getSituationTitle(situation: Situation, language: string): string {
  return getSituationText('title', situation, language, situation.title);
}

export function getSituationLocation(situation: Situation, language: string): string {
  return getSituationText('location', situation, language, situation.location);
}

export function getSituationUnitTitle(
  situation: Situation,
  language: string,
  t: TFunction,
): string {
  return getSituationText('unit', situation, language, situation.unit_title ?? t('situation.fallbackUnit'));
}

export function getSituationSearchText(situation: Situation, language: string, t: TFunction): string {
  return [
    getSituationTitle(situation, language),
    getSituationLocation(situation, language),
    getSituationUnitTitle(situation, language, t),
    situation.title,
    situation.location,
    situation.unit_title,
    ...situation.dialogue.map((line) => line.text),
    ...situation.puzzles.flatMap((puzzle) => [puzzle.sentence, ...puzzle.answer]),
  ].join(' ');
}
